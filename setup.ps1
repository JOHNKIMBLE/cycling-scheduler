Write-Host "  Output dir:     $OutputDirResolved"
Write-Host "  Local Deno:     $DenoBin"
Write-Host "  Env file:       $EnvFile"
Write-Host "  Runner file:    $RunnerFile"
Write-Host "  Cookie path:    $CookieFile"
Write-Host "  Log file:       $LogFile"
Write-Host ""
Write-Host "Usage:"
